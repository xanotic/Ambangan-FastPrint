import { type ReactNode } from "react";
import { motion } from "framer-motion";

interface ScrollFadeInProps {
  children: ReactNode;
  className?: string;
  delay?: number;
  direction?: "up" | "down" | "left" | "right" | "none";
  duration?: number;
  distance?: number;
}

/**
 * Wraps any content in a smooth fade-in animation triggered on scroll.
 * Mimics the cinematic text reveal seen on apple.com product pages.
 */
export default function ScrollFadeIn({
  children,
  className = "",
  delay = 0,
  direction = "up",
  duration = 0.8,
  distance = 40,
}: ScrollFadeInProps) {
  const directionMap = {
    up: { y: distance, x: 0 },
    down: { y: -distance, x: 0 },
    left: { x: distance, y: 0 },
    right: { x: -distance, y: 0 },
    none: { x: 0, y: 0 },
  };

  const offset = directionMap[direction];

  return (
    <motion.div
      initial={{ opacity: 0, ...offset }}
      whileInView={{ opacity: 1, x: 0, y: 0 }}
      viewport={{ once: true, margin: "-80px" }}
      transition={{
        duration,
        delay,
        ease: [0.25, 0.1, 0.25, 1],
      }}
      className={className}
    >
      {children}
    </motion.div>
  );
}
